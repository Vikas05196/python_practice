# range in python 

print("one argument.")

one = range(5)

for i in one:
    print(i)

print("two argument.")

two = range(1,20)    

for u in two:
    print(u)

print("three argument.")
three = range(1,20,5)

for w in three:
    print(w)

print("reverse argument.")   

reverse_range = range(100,50,-10)
for z in reverse_range:
    print(z)


